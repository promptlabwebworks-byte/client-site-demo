      <h3>
       <a id="logon" tabindex="-1"></a>Help information about signing in
      </h3>
      <p>
       <strong>Username: </strong>
      </p>
      <p>This is the Username that you created when you signed up for your GCKey.</p>
      <p>
       <strong>Password: </strong>
      </p>
      <p>This is the Password that you created when you signed up for your GCKey.</p>

      <h3>
       <a id="registering"></a>Help information about signing up for a GCKey
      </h3>
      <h4>Create Your Username:</h4>
      <p>When creating your Username, we recommend that you:</p>
      <ul>
       <li>make your Username easy for you to remember and hard for others to guess;</li>
       <li>
        avoid using personal information, such as your name, Social Insurance Number (SIN), mailing address, or email
        address;
       </li>
       <li>always keep your Username secure and do not share it with anyone.</li>
      </ul>
      <p>Rules for Username creation are located in the Username Checklist Box.</p>
      <p>As your Username selection is entered, the rules will either reflect a red “x” to indicate you have
       not met the requirement, or to a green checkmark to indicate the requirement has been met.</p>
      <p>You will not be able to proceed unless all rules are met.</p>
      <p>The following characters are permitted in your Username:</p>
      <ul>
       <li>Capital letters (A - Z)</li>
       <li>Lower-case letters (a - z)</li>
       <li>French characters (àâæçéèêëîïôœùûüÿÀÂÆÇÉÈÊËÎÏÔŒÙÛÜŸ)</li>
       <li>Digits (0 - 9)</li>
       <li>Special characters: $ % ^ : { } ~ ' ? / | &amp; * _ - . ` ( ) [ ] !</li>
                                </ul>
                                <p>Each Username must be unique. You will receive a message if it has already been used.
                                </p>
                                <h4>Create Your Password:</h4>
                                <p>When creating your Password, we recommend that you:</p>
                                <ul>
                                    <li>make your Password easy for you to remember and hard for others to guess;</li>
                                    <li>
                                        avoid using personal information, such as your name, Social Insurance Number
                                        (SIN), mailing address, or email
                                        address;
                                    </li>
                                    <li>keep your Password secure and do not share it with anyone.</li>
                                </ul>
                                <p>Rules for your Password creation are located in the Password Checklist Box.</p>
                                <p>As your Password selection is entered, the rules will either reflect a red “x” to
                                    indicate you have
                                    not met the requirement, or to a green checkmark to indicate the requirement has
                                    been met.</p>
                                <p>You will not be able to proceed unless all rules are met.</p>
                                <h4>Confirm Your Password:</h4>
                                <p>
                                    You must re-enter your Password exactly as you entered it into the <em>Create Your
                                        Password</em> field. If both
                                    Passwords do not match, you will not be able to proceed.
                                </p>
                                <h4>Create Recovery Questions, Answers and Hints:</h4>
                                <p>These are used to assist you when you forget your password.</p>
                                <p>Your questions, answers and hints should be easy for you to remember but hard for
                                    others to guess. You can
                                    optionally use hints to assist you with remembering your answers to these questions.
                                </p>
                                <h4>Optional: Add Email Address for Username and Password Recovery</h4>
                                <p>The email address is used to assist you when you forget your username or password.
                                    This is an optional step,
                                    and you can add your recovery email anytime by going to your main GCKey account page
                                    and navigating to the Manage
                                    Email section.</p>
                                <h4>Confirmation Code:</h4>
                                <p>This is the code sent to the recovery email address associated with your GCKey
                                    account. It is used to confirm
                                    your email for username and password recovery.</p>
                                <p>Note: If your email address is associated with another GCKey account, a warning
                                    message will appear indicating
                                    that it is already in use. You have the option to 1) attempt to recover the username
                                    and/or password of the other
                                    GCKey account by following the process for username recovery or password reset, 2)
                                    register a different email
                                    address or 3) register the email address to your new GCKey account by completing the
                                    email address registration
                                    process; completing this process will remove the email address from the other GCKey
                                    account </p>

