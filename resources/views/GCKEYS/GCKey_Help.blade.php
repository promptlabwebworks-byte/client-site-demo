                                <h3>
                                    <a id="registering-2fa"></a>Registering for two-factor authentication (2FA)
                                </h3>
                                <p>If you haven't already registered for two-factor authentication, the next time you
                                    access Government of Canada
                                    online services, you will need to register for 2FA.</p>
                                <h4>Here are the 2FA registration steps:</h4>
                                <h5>Step 1: Sign in with GCKey or create a new GCKey account</h5>
                                <ul>
                                    <li>Sign in with your existing GCKey account.</li>
                                    <li>Or, if you don't have a GCKey account, please sign-up(register) for a new GCKey
                                        account.</li>
                                </ul>
                                <h5>Step 2: Choose a two-factor authentication method</h5>
                                <p>Once you have successful signed in with your existing or new GCKey account, you will
                                    be presented to choose a
                                    two-factor authentication method. Here are the 2FA options:</p>
                                <ol type="a">
                                    <li>Use your smartphone or tablet (recommended)</li>
                                    <li>Use your desktop or laptop</li>
                                    <li>Use an email address</li>
                                </ol>
                                <h5>Step 3: Accept T&amp;Cs</h5>
                                <p>Once you have selected your preferred 2FA method, you will have to accept the "Terms
                                    and conditions of use" in
                                    order to proceed.</p>
                                <h5>Step 4: To enable your selected 2FA method</h5>
                                <ol type="a">
                                    <li>
                                        If you have selected to use your smartphone or tablet (recommended)
                                        <ol type="i">
                                            <li>You need an authenticator application to use on your smartphone or
                                                tablet.</li>
                                            <li>
                                                Using the authenticator application on your smartphone or tablet, scan
                                                the QR code on the screen or enter
                                                the secret key to generate your authentication code.
                                            </li>
                                            <li>
                                                On the "Register your device" page, you will have to enter the 6-digit
                                                code(authenticator code) from your
                                                authenticator app.
                                            </li>
                                            <li>Select "Continue" to confirm your 2FA method.</li>
                                        </ol>
                                    </li>
                                    <li>
                                        If you have selected to use your desktop or laptop
                                        <ol type="i">
                                            <li>You need an authenticator application to use on your desktop or laptop.
                                            </li>
                                            <li>
                                                Using the authenticator application on your desktop or laptop, enter the
                                                secret key to generate your
                                                authentication code.
                                            </li>
                                            <li>
                                                On the "Register your device" page, you will have to enter the 6-digit
                                                code(authenticator code) from your
                                                authenticator app.
                                            </li>
                                            <li>Select "Continue" to confirm your 2FA method.</li>
                                        </ol>
                                    </li>
                                    <li>
                                        If you have selected to use your email address
                                        <ol type="i">
                                            <li>
                                                Enter your email address and select "Continue". You will receive a one
                                                time 6-digit passcode on this
                                                email.
                                            </li>
                                            <li>
                                                On the "Verify your email address" page, you will have to enter the
                                                6-digit code(one-time passcode) that
                                                you received on your email.
                                            </li>
                                            <li>Select "Continue" to confirm your 2FA method.</li>
                                        </ol>
                                    </li>
                                </ol>
                                <h5>Step 5: Save your recovery codes</h5>
                                <p>After you enabled your two-factor authentication method, you will be shown a set of 5
                                    recovery codes. You can
                                    use these codes if you lose or damage your device, or if you delete or reinstall
                                    your authenticator app. It is
                                    highly recommend that you save these recovery codes somewhere safe and accessible.
                                </p>
                                <p>Once you confirm that you have safely stored your recovery codes, your registration
                                    is complete.</p>
                                <h4>What to expect after registering for two-factor authentication?</h4>
                                <p>Each time you sign in to your GCKey account, you will be prompted to enter an
                                    authentication code from the
                                    authenticator app or enter the one-time passcode sent to your email.</p>
                                <h4>If you didn't receive your one-time passcode via your email</h4>
                                <p>First, check your junk email folder. If you cannot find an email with the one-time
                                    passcode, you can resend the
                                    one-time passcode by selecting the Resend button. This will generate a new one-time
                                    passcode.</p>
                                <h4>If you need to recover or reset your two-factor authentication method</h4>
                                <p>You can recover or reset your two-factor authentication method if you have
                                    experienced any of the
                                    following:</p>
                                <ul>
                                    <li>Lost or replaced your device, or if your device was stolen.</li>
                                    <li>Deleted or re-installed the authenticator application.</li>
                                    <li>Lost access to your email.</li>
                                    <li>Want to use a different two-factor authentication option.</li>
                                </ul>
                                <h4>To recover or reset your two-factor authentication method:</h4>
                                <ol>
                                    <li>Sign in to your GCKey account.</li>
                                    <li>When prompted for your two-factor authentication code, select "Recover account".
                                    </li>
                                    <li>Enter one of the recovery codes you saved when you first set up two-factor
                                        authentication.</li>
                                    <li>
                                        If you want to change your device or authentication method, select "Change your
                                        two-factor authentication".
                                        <ol type="a">
                                            <li>You will be prompted you to set up two-factor authentication again.</li>
                                            <li>
                                                Once the method has been chosen and confirmed, new recovery codes will
                                                be generated. Save them in a safe
                                                place. Your old recovery codes will no longer work.
                                            </li>
                                        </ol>
                                    </li>
                                    <li>
                                        If you're running out of recovery codes, select "Get new recovery codes". To
                                        generate a new set of 5 recovery
                                        codes.
                                    </li>
                                    <li>You will be shown a set of 5 new recovery codes. Save them in a safe place.</li>
                                </ol>
