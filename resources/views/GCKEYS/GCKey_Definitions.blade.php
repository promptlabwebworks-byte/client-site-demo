                  <h1 id="wb-cont">Definitions</h1>
                  <div>
                     <section class="def-list">
                        <p>Shown below are definitions for terms that you may encounter during your visit with the GCKey. These definitions, which are updated periodically, may help you better understand the terms and their intended meanings within the GCKey service.</p>
                        <dl>
                           <dt>Access</dt>
                           <dd>Gaining entry to a government service using a web browser.</dd>
                           <dt>Bookmark</dt>
                           <dd>Add a favourite page to a list on your browser for quick and easy access to it later.</dd>
                           <dt>Cancel</dt>
                           <dd>A command shown on a button that stops the process you are in.</dd>
                           <dt>Clear</dt>
                           <dd>A command shown on a button that clears input or selected values on that page.</dd>
                           <dt>Continue</dt>
                           <dd>A command shown on a button that takes you to the next step in the process.</dd>
                           <dt>Cookie</dt>
                           <dd>A small piece of information that is sent to your computer from a website and is used by your browser.</dd>
                           <dt>Enrol</dt>
                           <dd>Is the action of signing up, or applying for Government services.</dd>
                           <dt>Exit</dt>
                           <dd>A command shown on a button that ends a process and exits the system.</dd>
                           <dt>Favourites</dt>
                           <dd>Add a favourite page to a list on your browser for quick and easy access to it later.</dd>
                           <dt>Sign In</dt>
                           <dd>Is the process of allowing you on line access to Enabled Services.</dd>
                           <dt>Sign In Information</dt>
                           <dd>Is the combination of your Username and Password.</dd>
                           <dt>Sign Out</dt>
                           <dd>Is the process of ending your on line access to Enabled Services.</dd>
                           <dt>Manage Your GCKey</dt>
                           <dd>Functions that allow you to Change Your Password; Change Your Recovery Questions; Register a Password and Account Recovery Email Address; Recover your Username or Password; Change Your Recovery Email Address; or Revoke Your GCKey.</dd>
                           <dt>Official Languages</dt>
                           <dd>Is the Government of Canada’s policy statement concerning both official languages.</dd>
                           <dt>Privacy Notice</dt>
                           <dd>Is the policy statement concerning the protection of personal information.</dd>
                           <dt>Recovery</dt>
                           <dd>Is the process of regaining access to your GCKey. This can be done by recovering your password via recovery questions, or recovering your username and/or password via email.</dd>
                           <dt>Revoke</dt>
                           <dd>Is the process of cancelling your GCKey and making it unusable. Revoked GCKeys cannot be recovered.</dd>
                           <dt>Sign Up</dt>
                           <dd>Is the process of obtaining a GCKey from the GCKey service.</dd>
                           <dt>Submit</dt>
                           <dd>A command shown on a button that submits your data to the system and moves you to the next step in the process.</dd>
                           <dt>TTY/TDD</dt>
                           <dd>Is a telecommunications device used by persons who are hearing impaired. A typewriter-like unit prints the conversation on screen or paper so that it can be read.</dd>
                        </dl>
